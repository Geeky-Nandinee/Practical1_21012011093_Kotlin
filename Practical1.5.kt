fun main() {
    print("Enter the Month Number:")
    val Number = readLine()!!.toString()
    when (Number) {
        "1" -> println("January")
        "2" -> println("Feb")
        "3" -> println("March")
        "4" -> println("Apr")
        "5" -> println("May")
        "6" -> println("june")
        "7" -> println("july")
        "8" -> println("Agust")
        "9" -> println("September")
        "10" -> println("Octombere")
        "11" -> println("Navember")
        "12" -> println("Desember")
    }
}